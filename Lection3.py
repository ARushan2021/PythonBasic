lst = ['spam', 'drums', '100', '112233445']
print(lst[1:3]) # вызов списка по индексам ['drums', '100']

lst [3] = '123' # замена обьекта в списке
print(lst) # ['spam', 'drums', '100', '123']

lst[1:1] = ['tyut01'] # вставка нового обьекта в список
lst[3:] = [333] # ['spam', 'drums', '100', '123']
print(lst) # ['spam', 'tyut01', 'drums', 333]

lst1 = lst[1:3] # создание нового списка из старого
print(lst1) # ['tyut01', 'drums']

lst2 = list(lst) # создание нового списка из старого
print(lst2*2) # ['spam', 'tyut01', 'drums', 333, 'spam', 'tyut01', 'drums', 333]

for i in lst: # цикл для списка
    print(i*2) # tyut01tyut01 \drumsdrums \666


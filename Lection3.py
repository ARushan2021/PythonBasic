lst5 = [i**2 for i in range(10)] # создание списка
print(lst5) # [0, 1, 4, 9, 16, 25, 36, 49, 64, 81]

lst5.append('ty') # добавление обьекта в конец списка
print(lst5) # [0, 1, 4, 9, 16, 25, 36, 49, 64, 81, 'ty']

lst5.insert(2, 100) # добавление обьекта в середину списка по индексу
print(lst5) # [0, 1, 100, 4, 9, 16, 25, 36, 49, 64, 81, 'ty']

print(lst5.count('ty')) # подсчитывать сколько раз значение указывается в списке \ 1

lst5.remove(lst5[8]) # удаление обьекта из списка по индексу или значению
lst5.remove('ty') # # удаление обьекта из списка по значению
print(lst5) # [0, 1, 100, 4, 9, 16, 25, 36, 64, 81]

lst5.sort(reverse=True) # сортировка по убыванию
lst5.sort # сортировка по возврастанию
print(lst5)

lst5.reverse() # разворот массива
print(lst5)

print(lst5.pop()) # последний элемент в списке, можно по номеру элемента,
# при использовании этой функции сам элемент удаляется из списка

